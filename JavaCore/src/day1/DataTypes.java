package day1;

public class DataTypes {

	public static void main(String[] args) {
		//all data types 
		//declaration datatype names variables name= value 
		byte a=2;
		short b=20;
		int c= 23456;
		long d= 12434566546654453L;
		
		float x= 12.34f;
		double y= 732.23;
		
		char ans= 'y';
		boolean valid= true;
		
		String name= "Hasan Esat Kayman";
		
		System.out.println("byte type " + a);
		System.out.println("short type " + b);
		System.out.println("int type " + c);
		System.out.println("long type " + d);
		System.out.println("float type " + x);
		System.out.println("double type " + y);
		System.out.println("char type " + ans);
		System.out.println("boolean type " + valid);
		System.out.println("string type " + name);

	}

}
